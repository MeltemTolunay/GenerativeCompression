def print_mean_metrics(metrics, method):
    for x in metrics.keys():
        if x!='size':
            print(method+': mean {}={}'.format(x, sum(metrics[x])/len(metrics[x])))
        else:
            print(method+': bpp {}={}'.format(x, 8*(sum(metrics[x])/len(metrics[x]))/(128*128))) # calculating bpp before zipping. Reported resutls are not from this script

